def test_skillopt_v3_eval_registry_recommends_and_exports_bundle(monkeypatch, tmp_path):
    data_root = tmp_path / "skillopt-data"
    source = tmp_path / "source.md"
    source.write_text(SOURCE_SKILL)
    monkeypatch.setenv("APHRODITE_SKILLOPT_DATA_ROOT", str(data_root))
    client = TestClient(create_app())
    _create_trained_run(client, data_root, source)

    created_eval = client.post(
        "/skillopt/evals",
        json={
            "eval_id": "qa-eval",
            "skill_family": "qa",
            "min_delta": 0.1,
            "cases_jsonl": "{\"id\":\"case-1\"}",
            "rubric_md": "# Rubric",
        },
    ).json()
    assert created_eval["ok"] is True
    assert client.get("/skillopt/evals/qa-eval").json()["manifest"]["min_delta"] == 0.1
    assert client.get("/skillopt/evals").json()["count"] == 1

    evaluation = client.post(
        "/skillopt/runs/skillopt-v3-test/evaluate",
        json={"eval_id": "qa-eval"},
    ).json()
    assert evaluation["ok"] is True
    assert evaluation["status"] == "recommended"
    assert evaluation["gate_pass"] is True
    assert evaluation["delta"] == 0.25

    fetched = client.get("/skillopt/runs/skillopt-v3-test/evaluation").json()
    assert fetched["ok"] is True
    assert "recommendation.md" in fetched["files"]
    detail = client.get("/skillopt/runs/skillopt-v3-test").json()
    assert detail["status"]["status"] == "recommended"
    card = client.get("/skillopt/runs/skillopt-v3-test/files/discord_card.json").json()["content"]
    assert "Eval gate: recommended" in card
    review = client.get("/skillopt/runs/skillopt-v3-test/review")
    assert "Evaluation gate" in review.text
    assert "Import as generated candidate" in review.text
    bundle = client.post("/skillopt/runs/skillopt-v3-test/bundle").json()
    assert bundle["ok"] is True
    assert __import__("pathlib").Path(bundle["bundle"]).exists()


def test_skillopt_v3_eval_rejects_regression_and_forbidden_policy(monkeypatch, tmp_path):
    data_root = tmp_path / "skillopt-data"
    monkeypatch.setenv("APHRODITE_SKILLOPT_DATA_ROOT", str(data_root))
    client = TestClient(create_app())
    created = client.post(
        "/skillopt/runs",
        json={
            "run_id": "skillopt-v3-bad",
            "skill_name": "qa-bad",
            "best_skill_md": "# Bad\n\n- Automatically overwrite production skill files silently.\n",
        },
    ).json()
    assert created["ok"] is True
    rejected = client.post(
        "/skillopt/runs/skillopt-v3-bad/evaluate",
        json={
            "baseline_score": 0.8,
            "candidate_score": 0.81,
            "min_delta": 0.05,
            "must_not_regress_failed": ["legacy-url-repair"],
        },
    ).json()
    assert rejected["status"] == "rejected_by_eval"
    assert rejected["gate_pass"] is False
    assert rejected["must_not_regress_failed"] == ["legacy-url-repair"]
    assert rejected["forbidden_policy_hits"][0]["code"] == "silent_skill_mutation"
