from __future__ import annotations

import sys

from fastapi.testclient import TestClient

from aphrodite.app import create_app
from aphrodite.modules import skillopt


BEST_SKILL = """# Question Answering Skill\n\n- Prefer concise answers.\n"""
SOURCE_SKILL = """---\nname: old-qa\ndescription: \"Old skill.\"\n---\n\n# Old\n\n- Be vague.\n"""


def test_skillopt_run_wraps_best_skill_and_imports_candidate(monkeypatch, tmp_path):
    data_root = tmp_path / "skillopt-data"
    profile = tmp_path / "profile"
    source = tmp_path / "source.md"
    source.write_text(SOURCE_SKILL)
    monkeypatch.setenv("APHRODITE_SKILLOPT_DATA_ROOT", str(data_root))

    client = TestClient(create_app())
    response = client.post(
        "/skillopt/runs",
        json={
            "run_id": "skillopt-test-run",
            "skill_name": "qa-optimized",
            "description": "Optimized QA behavior generated by SkillOpt.",
            "source_skill_path": str(source),
            "best_skill_md": BEST_SKILL,
        },
    )

    assert response.status_code == 200
    created = response.json()
    assert created["ok"] is True
    assert created["status"] == "candidate_ready"
    run_dir = data_root / "runs" / "skillopt-test-run"
    candidate = (run_dir / "candidate_SKILL.md").read_text()
    assert candidate.startswith("---\nname: qa-optimized\n")
    assert "# Question Answering Skill" in candidate
    assert "source_skill_path" in (run_dir / "request.json").read_text()
    assert "best_skill_md" not in (run_dir / "request.json").read_text()

    diff = client.get("/skillopt/runs/skillopt-test-run/diff").json()
    assert diff["ok"] is True
    assert "candidate_SKILL.md" in diff["diff"]
    assert "Prefer concise answers" in diff["diff"]

    imported = client.post(
        "/skillopt/runs/skillopt-test-run/import",
        json={"profile_home": str(profile)},
    ).json()
    assert imported["ok"] is True
    destination = profile / "skills" / "generated" / "qa-optimized" / "SKILL.md"
    assert imported["destination"] == str(destination)
    assert destination.read_text() == candidate
    assert "fresh Hermes session" in imported["activation"]


def test_skillopt_import_refuses_overwrite_without_replace(monkeypatch, tmp_path):
    monkeypatch.setenv("APHRODITE_SKILLOPT_DATA_ROOT", str(tmp_path / "data"))
    profile = tmp_path / "profile"
    first = skillopt.create_run(
        {"run_id": "skillopt-overwrite", "skill_name": "same-skill", "best_skill_md": BEST_SKILL}
    )
    assert first["ok"] is True
    assert skillopt.import_candidate("skillopt-overwrite", {"profile_home": str(profile)})["ok"] is True

    refused = skillopt.import_candidate("skillopt-overwrite", {"profile_home": str(profile)})
    assert refused["ok"] is False
    assert refused["error_type"] == "exists"

    replaced = skillopt.import_candidate("skillopt-overwrite", {"profile_home": str(profile), "replace": True})
    assert replaced["ok"] is True


